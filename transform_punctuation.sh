#!/bin/bash
sed -i 's/,[[:blank:]]*/，/g' "$@" # 任何,都被替换
sed -i 's/?[[:blank:]]*/？/g' "$@" # 任何?都被替换
sed -i 's/![[:blank:]]+/！/g' "$@" # !后有空格则可以被替换
sed -i 's/!\([^[]\)/！\1/g' "$@"   # !不跟[则可以被替换，但是由于图片的格式是![,因此不能被替换
# 由于.用户划分域名，因此不能被随意替换
sed -i 's/\.[[:blank:]]+/。/g' "$@" # .后有空格则可以被替换
sed -i 's/\.$/。/g' "$@"            # .在行尾也能被替换
sed -i 's/\.\([^[:alpha:][:blank:][:cntrl:][:punct:][:digit:]]\)/。\1/g' "$@" # .后跟汉字也能被替换
